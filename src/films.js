export const getCategories = () => {
  let arr = [];
  filmData.forEach((film) => {
    let genreArr = film.genre.split(',');
    genreArr = genreArr.map((genre) => genre.trim());
    genreArr.forEach((genre) => !arr.includes(genre) && arr.push(genre));
  });

  return arr;
};

export const getCountries = () => {
  let arr = [];
  filmData.forEach((film) => {
    let countryArr = film.country.split(',');
    countryArr = countryArr.map((country) => country.trim());

    countryArr.forEach(
      (country) => !arr.includes(country) && arr.push(country)
    );
  });

  return arr;
};

